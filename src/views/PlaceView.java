    private void loadPlaces() {
        if (this.places != null) {
            this.places.clear();
        }

        this.places = Place.getAll(connection);

        DefaultTableModel model = (DefaultTableModel) placeTable.getModel();

        model.setRowCount(0);

        for (Place place : places) {

            Object[] row = new Object[4];
            row[0] = place.getId();
            row[1] = place.getName();
            row[2] = place.getProvince();

            model.addRow(row);
        }
    }
    
